class Skill {
  final String name;
  final String defLvl;
  final int sp;
  final int mtDamage;
  final int rngRange;
  final String effect;
  final bool exclusive;
  final String moveType;
  final String weaponType;

  Skill({this.name,this.defLvl,this.sp,this.mtDamage,this.rngRange,this.effect,this.exclusive,this.moveType,this.weaponType});
}